#include "3-calc.h"

/**
 * op_add - adding
 * @a: int1
 * @b: int2
 * Return: sum
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - minasing
 * @m: int1
 * @a: int2
 * Return: sub
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - producting
 * @a: int1
 * @b: int2
 * Return: product
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - diviaation
 * @a: int1
 * @b: int2
 * Return: diviation
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_add - mod
 * @a: int1
 * @b: int2
 * Return: mod
 */
int op_mod(int a, int b)
{
return (a % b);
}
